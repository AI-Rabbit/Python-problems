s = input()
a = int(input())

length = len(s)

if length > a:
    print('True')
else:
    print('False')

if length >= a:
    print('True')
else:
    print('False')

if length == a:
    print('True')
else:
    print('False')

if length != a:
    print('True')
else:
    print('False')
